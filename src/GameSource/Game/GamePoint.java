public class GamePoint {
        
        int x, y, z;
        
        public GamePoint(int _x, int _y, int _z) {
                x = _x;
                y = _y;
                z = _z;
        }
        
        public int getX() {
                return x;
        }
        
        public int getY() {
                return y;
        }
        
        public int getZ() {
                return z;
        }
}